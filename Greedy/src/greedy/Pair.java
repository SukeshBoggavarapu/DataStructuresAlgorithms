package greedy;

public class Pair {
	
	int vertexNumber;
	int weight;
	
	public Pair(int vertexNumber,int weight){
		this.vertexNumber=vertexNumber;
		this.weight=weight;
	}

	public int getVertexNumber() {
		return vertexNumber;
	}

	public void setVertexNumber(int vertexNumber) {
		this.vertexNumber = vertexNumber;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

}
